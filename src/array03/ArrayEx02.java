package array03;

/**
 * 
 * @author for문의 정리
 * for문은 표현식으로 호출하지 않아도 실행이된다.
 * 초기값은 처음 실행될때, 증감값은 for문끝의 }이 될때 실행된다.
 * 내부의 선언된 변수는 지역변수이다.
 * 한번 실행되고나면 조건만 보면된다.
 */

public class ArrayEx02 {

	public static void main(String[] args) {

		for (int i = 0; i < 10; i++) {
			System.out.println(i);
		}
	}

}
